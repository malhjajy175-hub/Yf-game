def reshape_ar(text: str) -> str:
    """يرتب حروف النص العربي بشكل متصل وصحيح الاتجاه قبل رسمه بـ PIL."""
    reshaped = arabic_reshaper.reshape(text)
    return get_display(reshaped)


def load_font(size: int) -> ImageFont.FreeTypeFont:
    try:
        return ImageFont.truetype(FONT_PATH, size)
    except Exception:
        # نسخة احتياطية: خط النظام (يدعم العربية غالباً على أغلب الأنظمة الحديثة)
        for fallback in (
            "/usr/share/fonts/truetype/noto/NotoNaskhArabic-Bold.ttf",
            "/usr/share/fonts/truetype/kacst/KacstOne.ttf",
        ):
            if os.path.exists(fallback):
                return ImageFont.truetype(fallback, size)
        # آخر حل: خط افتراضي (لن يدعم العربية بشكل مثالي، لكن أفضل من كراش)
        return ImageFont.load_default()


def make_image(question: str) -> discord.File:
    img = Image.open(BG_PATH).convert("RGBA")
    draw = ImageDraw.Draw(img)
    W, H = img.size

    font_q = load_font(60)

    GOLD = (255, 215, 0, 255)
    SHADOW = (0, 0, 0, 200)

    # السؤال في الوسط مع تقطيع السطور
    lines = textwrap.wrap(question, width=25)
    shaped_lines = [reshape_ar(l) for l in lines]

    total_h = sum(
        [
            draw.textbbox((0, 0), l, font=font_q)[3]
            - draw.textbbox((0, 0), l, font=font_q)[1]
            + 10
            for l in shaped_lines
        ]
    )
    y = (H - total_h) / 2

    for line in shaped_lines:
        bbox2 = draw.textbbox((0, 0), line, font=font_q)
        lw = bbox2[2] - bbox2[0]
        lh = bbox2[3] - bbox2[1]
        x = (W - lw) / 2
        draw.text((x + 3, y + 3), line, font=font_q, fill=SHADOW)
        draw.text((x, y), line, font=font_q, fill=GOLD)
        y += lh + 10

    buf = io.BytesIO()
    img.save(buf, format="PNG")
    buf.seek(0)
    return discord.File(buf, filename="kat.png")


class KatCog(commands.Cog):
    def __init__(self, bot: commands.Bot):
        self.bot = bot

    @commands.command(name="كت")
    async def kat(self, ctx: commands.Context):
        question = random.choice(QUESTIONS)
        # نشغل معالجة الصورة في thread منفصل حتى ما يتجمدش البوت على باقي الأوامر
        file = await asyncio.to_thread(make_image, question)
        await ctx.send(file=file)


async def setup(bot: commands.Bot):
    await bot.add_cog(KatCog(bot))
