async def make_image(club_id: int) -> discord.File:
    img = get_bg()
    draw = ImageDraw.Draw(img)
    W, H = img.size

    try:
        font_title = ImageFont.truetype("/usr/share/fonts/truetype/dejavu/DejaVuSans-Bold.ttf", 60)
    except:
        font_title = ImageFont.load_default()

    GOLD = (255, 215, 0, 255)
    SHADOW = (0, 0, 0, 200)

    title = "شعار"
    bbox = draw.textbbox((0, 0), title, font=font_title)
    tw = bbox[2] - bbox[0]
    tx = (W - tw) / 2
    ty = H * 0.05
    draw.text((tx+3, ty+3), title, font=font_title, fill=SHADOW)
    draw.text((tx, ty), title, font=font_title, fill=GOLD)

    logo_url = f"https://media.api-sports.io/football/teams/{club_id}.png"
    async with aiohttp.ClientSession() as session:
        async with session.get(logo_url) as resp:
            if resp.status != 200:
                raise Exception("Logo not found")
            logo_data = await resp.read()

    logo_img = Image.open(io.BytesIO(logo_data)).convert("RGBA")
    logo_size = int(min(W, H) * 0.5)
    logo_img = logo_img.resize((logo_size, logo_size))
    lx = (W - logo_size) // 2
    ly = (H - logo_size) // 2 + 20
    img.paste(logo_img, (lx, ly), logo_img)

    buf = io.BytesIO()
    img.save(buf, format="PNG")
    buf.seek(0)
    return discord.File(buf, filename="logo.png")

class Sha3arCog(commands.Cog):
    def __init__(self, bot):
        self.bot = bot

    @commands.command(name="شعار")
    async def sha3ar(self, ctx):
        channel_id = ctx.channel.id
        if active_games.get(channel_id):
            await ctx.send("⚠️ هناك لعبة قيد التشغيل!")
            return
        active_games[channel_id] = True
        try:
            club_name, club_id = random.choice(CLUBS)
            try:
                file = await make_image(club_id)
            except:
                active_games[channel_id] = False
                await ctx.send("⚠️ حدث خطأ في تحميل الشعار، حاول مرة ثانية!")
                return

            await ctx.send(file=file)

            def check(m):
                return m.channel.id == channel_id and not m.author.bot and m.content.strip() == club_name

            end_time = asyncio.get_event_loop().time() + 10
            winner = None
            while True:
                remaining = end_time - asyncio.get_event_loop().time()
                if remaining <= 0:
                    break
                try:
                    msg = await self.bot.wait_for("message", timeout=remaining, check=check)
                    winner = msg.author
                    break
                except asyncio.TimeoutError:
                    break

            if winner:
                await add_solo_points(winner.id)
                await ctx.send(f"✅ إجابة صحيحة! {winner.mention} فاز بنقطة (+1)")
            else:
                await ctx.send(f"🔴 انتهى الوقت! النادي كان: **{club_name}**")
        finally:
            active_games[channel_id] = False

async def setup(bot):
    await bot.add_cog(Sha3arCog(bot))
